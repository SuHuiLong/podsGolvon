#import "SearchResultModel.h"

@implementation SearchResultModel

+(SearchResultModel *)initWithDictionary:(NSDictionary *)dic{
    SearchResultModel *model = [[SearchResultModel alloc]init];
    
    model.nickname = dic[@"nickname"];
    model.city = dic[@"city"];
    model.gender = dic[@"gender"];
    model.interview_state = dic[@"vid"];
    model.pic150Url = dic[@"avator"];
    model.pole_number = dic[@"rodnum"];
    model.province = dic[@"province"];
    model.nameID = dic[@"uid"];
    
    return model;
}


@end
